package com.syntax.class07;

public class ForLoopInClass {

	public static void main(String[] args) {

		/*initialization
		 * condition
		 * increment or decrement ex: for(for (int i=1; i<=5; i++)
		 */
		
		for (int i=2; i<=5; i++) {
			System.out.println("Hello");
		}
		
		
		System.out.println("I need to print numbers from 1 to 20");
		
		//start point
		//end point
		//increment or decrement
		
		for (int a=1; a<=20; a++) {
			System.out.println(a+" ");
		}
		
		System.out.println("I need to print numbers from 10 to 25");
		
		for (int b=10; b<=25; b++) {
			System.out.print(b+" ");
		}
		
		System.out.println("");
		
		System.out.println("I need to print numbers from 50 to 1.");
		
		for (int c=50; c>=1; c--) {
			System.out.print(c+" ");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
