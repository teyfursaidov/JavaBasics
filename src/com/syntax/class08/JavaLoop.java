package com.syntax.class08;

public class JavaLoop {

	public static void main(String[] args) {


		
		for (int i=0; i<=10; i+=3) {
			System.out.println(i+" ");
		}
		
		
		
		int sum=0;
		for (int i=1; i<=5; i++) {
			sum+=i;
		}
		
		System.out.println(sum);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
