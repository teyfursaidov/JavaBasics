package com.syntax.class09;

public class ClockLoop {

	public static void main(String[] args) {


		
		for (int a=0; a<=12; a++) {
			for (int b=0; b<60; b++) {
				for (int c=0; c<60; c++) {
					System.out.println(a+" : "+b+" : "+c);
				}
			}
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
				
		
	}

}
