package com.syntax.class09;

public class ForLoopExamples {

	public static void main(String[] args) {

		for (int i=0; i<=2; i++) {
			for (int j=1; j<=2; j++) {
				if (j==2) {
					continue; // break; CE: not reachable code
				}
				System.out.println("Good morning.");
			}
			System.out.println("Hello.");
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
