		// 3rd row or 3rd array
			numbers[2][0]=1000;
			numbers[2][1]=2000;
			numbers[2][2]=3000;
			numbers[2][3]=4000;
			
		System.out.println(numbers[1][2]);  //3001
		
		System.out.println(numbers[0][1]+numbers[1][0]);  //1202
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
