package com.practice101;

public class MultiplicationTable {

	public static void main(String[] args) {

		// 10 by 10 multiplication table
		
		
		for (int i=1; i<=10; i++) {
			for (int j=1; j<=10; j++) {
				System.out.print((i*j)+" ");
			}
			System.out.println();
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
